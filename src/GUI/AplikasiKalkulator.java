    private void hitungButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungButtonActionPerformed
        operandDua = layarTF.getText();
        Double doubleOperandSatu = Double.parseDouble(operandSatu);
        Double doubleOperandDua = Double.parseDouble(operandDua);
        if (!counted) {
            switch (operator) {
                case "+":
                    hasil = doubleOperandSatu + doubleOperandDua;
                    counted = true;
                    break;
                case "-":
                    hasil = doubleOperandSatu - doubleOperandDua;
                    counted = true;
                    break;
                case "*":
                    hasil = doubleOperandSatu * doubleOperandDua;
                    counted = true;
                    break;
                case "/":
                    hasil = doubleOperandSatu / doubleOperandDua;
                    counted = true;
                    break;
                default:
                    layarTF.setText("Invalid");
                    break;
            }
        } else {
            layarTF.setText(layarTF.getText());
        }
        if (Double.toString(hasil).contains(".0")) {
            layarTF.setText(Integer.toString((int) hasil));
        } else {
            layarTF.setText(Double.toString(hasil));
        }
    }//GEN-LAST:event_hitungButtonActionPerformed

