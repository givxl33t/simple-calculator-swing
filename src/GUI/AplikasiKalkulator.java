    private void persenButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_persenButtonActionPerformed
        operandDua = layarTF.getText();
        Double doubleOperandSatu = Double.parseDouble(operandSatu);
        Double doubleOperandDua = Double.parseDouble(operandDua);
        Double persenOperandDua = doubleOperandDua/100.0d;
        Double persenOperandDuaDenganSatu = doubleOperandSatu * persenOperandDua;
        
        if (!counted) {
            switch (operator) {
                case "+":
                    hasil = doubleOperandSatu + persenOperandDuaDenganSatu;
                    counted = true;
                    break;
                case "-":
                    hasil = doubleOperandSatu - persenOperandDuaDenganSatu;
                    counted = true;
                    break;
                case "*":
                    hasil = doubleOperandSatu * persenOperandDua;
                    counted = true;
                    break;
                case "/":
                    hasil = doubleOperandSatu / persenOperandDua;
                    counted = true;
                    break;
                default:
                    layarTF.setText("Invalid");
                    break;
            }       
        } else {
            layarTF.setText(layarTF.getText());
        }
        if (Double.toString(hasil).contains(".0")) {
            layarTF.setText(Integer.toString((int) hasil));
        } else {
            layarTF.setText(Double.toString(hasil));
        }
    }//GEN-LAST:event_persenButtonActionPerformed

